import turtle as t
import math
screen=t.Screen()
screen.setup(1000,800)
t.tracer(n=5,delay=0)
def tri(a,b,c,color):
    t.pu()
    t.goto(a[0],a[1])
    t.pd()
    t.fillcolor(color)
    t.begin_fill()
    for point in [a,b,c,a]:
        t.goto(point[0],point[1])
    t.end_fill()
def square(x,y,a,angle,color):
    t.pu()
    t.goto(x,y)
    t.setheading(angle)
    t.pd()
    t.fillcolor(color)
    t.begin_fill()
    for point in range(4):
        t.right(90)
        t.fd(a)
    t.end_fill()
def parallelogram (x,y,a,b,angle,color):
    t.up()
    t.goto(x,y)
    t.pd()
    t.setheading(angle)
    t.fillcolor(color)
    t.begin_fill()
    t.fd(a)
    t.left(45)
    t.fd(b)
    t.left(135)
    t.fd(a)
    t.left(45)
    t.fd(b)
    t.end_fill()
def romb(x, y, side, angle_of_romb, start_angle, color):
    t.setheading(start_angle)
    t.penup()
    t.goto(x,y)
    t.pendown()
    t.fillcolor(color)
    t.begin_fill()

    t.left((180-angle_of_romb)/2)
    t.forward(side)
    t.left(angle_of_romb)
    t.forward(side)
    t.left(180-angle_of_romb)
    t.forward(side)
    t.left(angle_of_romb)
    t.forward(side)
    t.end_fill()
def trapeze(x, y, low_osn, up_osn, side, start_angle, color):
    angle_radians = math.acos((low_osn - up_osn)/(2*side))
    angle = math.degrees(angle_radians)
    t.setheading(start_angle)
    t.penup()
    t.goto(x,y)
    t.pendown()
    t.fillcolor(color)
    t.speed(1)
    t.begin_fill()

    t.forward(low_osn)
    t.left(180 - angle)
    t.forward(side)
    t.left(angle)
    t.forward(up_osn)
    t.left(angle)
    t.forward(side)
    t.end_fill()
def rectangle (x,y,a,b,angle,color):
    t.setheading(angle)
    t.up()
    t.goto(x,y)
    t.pd()
    t.fillcolor(color)
    t.begin_fill()
    t.fd(a)
    t.left(90)
    t.fd(b)
    t.left(90)
    t.fd(a)
    t.left(90)
    t.fd(b)
    t.end_fill()
def ship():
    trapeze(0,200,60,120,40, 0, 'brown')
    rectangle(28,226.45,4,60,0,'black')
    tri((32,286.45), (55,276.45), (32, 266.45), 'indigo')
    tri((32,266.45), (60,256.45), (32, 246.45), 'indigo')
    rectangle(32,205, 30,5,0,'red')
    rectangle(32,210, 30,5,0,'blue')
    rectangle(32,215, 30,5,0,'white')
def center_cube():
    tri((0,0),(0,100),(50,50),'yellow')
    tri((0,100),(100,100),(50,50),'red')
    tri((100,100),(100,50),(75,75),'purple')
    tri((100,0),(100,50),(50,0),'lightblue')
    tri((50,50),(75,25),(25,25),'pink')
    square(75,75,35,45,'orange')
    parallelogram(0,0,50,35,0,'green')
def cat():
    parallelogram(150,50,25,20,300,'lightblue')
    tri((175,25),(200,50),(200,0),'orange')
    tri((205,50),(230,25),(205,0),'red')
    square(200,80,20,45,'lightgreen')
    tri((185,65),(185,90),(200,80),'orange')
    tri((215,65),(215,90),(200,80),'purple')
center_cube()
cat()
ship()
t.goto(500,500)
t.done()