def piece_of_snowflake(x, y, a, b, angle, color):
    t.pu()
    t.goto(x, y)
    t.pd()
    t.setheading(angle)
    t.fillcolor(color)
    t.begin_fill()
    t.fd(a)
    t.right(30)
    t.fd(b)
    t.circle(2, 180)
    t.fd(b)
    t.right(150)
    t.fd(b)
    t.circle(2, 180)
    t.fd(b)
    t.right(150)
    t.fd(b)
    t.circle(2, 180)
    t.fd(b)
    t.right(30)
    t.fd(a)
    t.circle(5, 180)
