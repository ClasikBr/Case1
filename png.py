def flower(x, y, a, color):
    t.pu()
    t.goto(x,y)
    t.pd()
    t.seth(120)
    t.fillcolor(color)
    t.begin_fill()
    t.fd(a)
    t.rt(30)
    t.fd(2 * a)
    t.rt(150)
    t.fd(a * 0.5)
    t.lt(120)
    t.fd(a * 0.5)
    t.rt(120)
    t.fd(a * 0.5)
    t.lt(120)
    t.fd(a * 0.5)
    t.rt(150)
    t.fd(2 * a)
    t.rt(30)
    t.fd(a)
    t.dot(1)
    t.end_fill()
def tri(a,b,c,color):
    t.pu()
    t.goto(a[0],a[1])
    t.pd()
    t.fillcolor(color)
    t.begin_fill()
    for point in [a,b,c,a]:
        t.goto(point[0],point[1])
    t.end_fill()
def square(x,y,a,angle,color):
    t.pu()
    t.goto(x,y)
    t.setheading(angle)
    t.pd()
    t.fillcolor(color)
    t.begin_fill()
    for point in range(4):
        t.right(90)
        t.fd(a)
    t.end_fill()
def parallelogram (x,y,a,b,angle,color):
    t.up()
    t.goto(x,y)
    t.pd()
    t.setheading(angle)
    t.fillcolor(color)
    t.begin_fill()
    t.fd(a)
    t.left(45)
    t.fd(b)
    t.left(135)
    t.fd(a)
    t.left(45)
    t.fd(b)
    t.end_fill()
def romb(x, y, side, angle_of_romb, start_angle, color):
    t.setheading(start_angle)
    t.penup()
    t.goto(x,y)
    t.pendown()
    t.fillcolor(color)
    t.begin_fill()
    t.left((180-angle_of_romb)/2)
    t.forward(side)
    t.left(angle_of_romb)
    t.forward(side)
    t.left(180-angle_of_romb)
    t.forward(side)
    t.left(angle_of_romb)
    t.forward(side)
    t.end_fill()
def trapeze(x, y, low_osn, up_osn, side, start_angle, color):
    angle_radians = math.acos((low_osn - up_osn)/(2*side))
    angle = math.degrees(angle_radians)
    t.setheading(start_angle)
    t.penup()
    t.goto(x,y)
    t.pendown()
    t.fillcolor(color)
    t.begin_fill()
    t.forward(low_osn)
    t.left(180 - angle)
    t.forward(side)
    t.left(angle)
    t.forward(up_osn)
    t.left(angle)
    t.forward(side)
    t.end_fill()
def rectangle (x,y,a,b,angle,color):
    t.setheading(angle)
    t.up()
    t.goto(x,y)
    t.pd()
    t.fillcolor(color)
    t.begin_fill()
    t.fd(a)
    t.left(90)
    t.fd(b)
    t.left(90)
    t.fd(a)
    t.left(90)
    t.fd(b)
    t.end_fill()
